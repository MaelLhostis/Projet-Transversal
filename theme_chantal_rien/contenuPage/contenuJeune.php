<div class="w3-container">
<p style="color: #3f51b5; font-size: 22px; margin-top: 50px; text-indent: 16px; margin-bottom: 0px;">Cette proposition s'adresse aux collèges et lycée, à la formation professionnelle, à l'enseignement supérieur, aux structures sociales, aux collectivités territoriales et services jeunesse :</p>


<ul class="w3-ul" style="margin-left: 50px;">
 	<li id="liPage2" class=" w3-border-0"><i id="icon1" class="fa fa-arrow-right"></i>Elle vise à proposer une réponse de prévention singulière et adaptée à chacun.</li>
 	<li id="liPage2" class=" w3-border-0"><i id="icon1" class="fa fa-arrow-right"></i>La démarche repose sur une approche globale du jeune et de sa santé ainsi que sur la participation des professipnnels et des familles.</li>
</ul>

<p style="text-indent: 16px; font-size: 18px; margin-left: 0px;">Elle s'inscrit dans le champs de la promotion santé et recouvre une diversité de problématiques: réflexions sur les comportements à risques, les addictions, les passages à l'acte, les relations entre jeunes, la prévention du harcèlement, l'estime de soi, la confiance en soi, capacité à demander e l'aide... La promotion de la santé renvoie à une vision globale de la santé physique, psychologique et sociale, à la participation des personnes aux actions de santé qui les concernent et au renforcement des compétences psycho-sociales.</p>

